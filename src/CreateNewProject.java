    private void BrowseLocationActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BrowseLocationActionPerformed
        projectname = Projectname.getText();

        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        int option = fileChooser.showDialog(this, "Open");
        if (option == JFileChooser.APPROVE_OPTION) {

            // The File class is used to create a new folder with the specified name in the selected directory            
            newFolder = fileChooser.getSelectedFile();
            //Retrieving just the name of the folder to be created (jtree)
            String folderpath = newFolder.getPath();
            //folderName = newFolder.getName();
            ProjectLoc.setText(folderpath);
            newprojectpath = folderpath + "\\" + projectname;
            Projectfolder.setText(newprojectpath);
        }


    }//GEN-LAST:event_BrowseLocationActionPerformed

    private void EstChrActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EstChrActionPerformed
//Start Here: Job submission
        JobManager jobManagerChr = new JobManager();
        //ServerConfigurationInputs.serverURL + "/estimate_chr/" + queryfilename + "/ChrEst.csv");
        //("http://elvis.misc.cranfield.ac.uk/group2vm/estimate_chr/Ridaeus_Ras1_v1.fasta/ChrEst.csv");
        //estimate_chr/:input_file/:output_file

        try {
            
            HashMap list = null;

            list = jobManagerChr.getJobOutputCSV(ServerConfigurationInputs.serverURL + "/download/ChrEst.csv");
            System.out.println("\nList:\n" + list);
     
            ArrayList<String> seqidlist = (ArrayList<String>) list.get(1);
            System.out.println("List: " + seqidlist);
            String[] estseqid = new String[seqidlist.size()];
            seqidlist.toArray(estseqid);

            ArrayList<String> seqlenlist = (ArrayList<String>) list.get(2);
            System.out.println("List: " + seqlenlist);
            String[] estseqlen = new String[seqlenlist.size()];
            seqlenlist.toArray(estseqlen);


            numofchromosomes = estseqid.length;
            jTextField1.setText(Integer.toString(numofchromosomes));

            String[] columnNames = {"Select", "Seq ID",
                "Seq Length"};

            Object[][] data = new Object[seqidlist.size()][5];

            for (int i = 0; i < seqidlist.size(); i++) {
                data[i][0] = true;
                data[i][1] = estseqid[i].replace("\"", "");
                data[i][2] = estseqlen[i].replace("\"", "");
            }

            DefaultTableModel table = new DefaultTableModel(data, columnNames) {
                @Override
                public Class getColumnClass(int column) {
                    return switch (column) {
                        case 0 ->
                            Boolean.class;
                        case 1 ->
                            String.class;
                        case 2 ->
                            Integer.class;
                        default ->
                            Boolean.class;
                    };
                }

                @Override
                public boolean isCellEditable(int row, int column) {
                    return column == 0;
                }
            };
            estchrTable.setModel(table);

            estchrTable.getModel().addTableModelListener(new TableModelListener() {
                @Override
                public void tableChanged(TableModelEvent e) {
                    confirmselid = new ArrayList<String>();
                    confirmsellen = new ArrayList<String>();
                    numofchromosomes = 0;
                    for (int i = 0; i < estchrTable.getModel().getRowCount(); i++) {
                        if ((Boolean) estchrTable.getModel().getValueAt(i, 0) == true) {
                            numofchromosomes++;
                            String selectedid = (String) estchrTable.getModel().getValueAt(i, 1);
                            String selectedlen = (String) estchrTable.getModel().getValueAt(i, 2);
                            confirmselid.add(selectedid);
                            confirmsellen.add(selectedlen);
                        }
                        System.out.println(confirmselid);
                        System.out.println(confirmsellen);
                    }
                    System.out.println(numofchromosomes);
                    jTextField1.setText(Integer.toString(numofchromosomes));

                }
            });

        } catch (Exception e) {
            e.printStackTrace();
        }// End Job Submission

    }//GEN-LAST:event_EstChrActionPerformed

