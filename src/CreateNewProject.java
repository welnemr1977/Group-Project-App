    private void OpenQueryFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_OpenQueryFileActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        FileNameExtensionFilter filter = new FileNameExtensionFilter("Fasta Files", "fasta", "fa");
        fileChooser.setFileFilter(filter);
        int result = fileChooser.showDialog(jPanel1, "Open");

        if (result == JFileChooser.APPROVE_OPTION) {
            File selectedFile = fileChooser.getSelectedFile();
            // save file path into a variable
            queryfilepath = selectedFile.getPath();
            queryfilename = selectedFile.getName();
            System.out.println(queryfilepath);
            queryfile.setText(queryfilepath);
            System.out.println(queryfilename);
        }
    }//GEN-LAST:event_OpenQueryFileActionPerformed

