    private void OpenQueryFastaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_OpenQueryFastaActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        int result = fileChooser.showDialog(jPanel1, "Open");

        if (result == JFileChooser.APPROVE_OPTION) {
            File selectedFile = fileChooser.getSelectedFile();
            // save file path into a variable
            //String QueryfilePath = selectedFile.getPath();
            QueryfilePath = selectedFile.getPath();
            System.out.println(QueryfilePath);
            jTextField1.setText(selectedFile.getName());
        } else if (result == JFileChooser.CANCEL_OPTION) {
            System.out.println("File already exists.");
        }

    }//GEN-LAST:event_OpenQueryFastaActionPerformed

