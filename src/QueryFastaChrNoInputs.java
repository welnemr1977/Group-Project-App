    private void ConfirmChrNoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConfirmChrNoActionPerformed
        String chromText = jTextField2.getText();
        JPanel panel = new JPanel();
        if (chromText.isEmpty()) {
            JOptionPane.showMessageDialog(panel, "Please enter a chromosome number.");
            return;
        }

        try {
            chrom = Integer.parseInt(chromText); // assign the parsed integer value inside the try block
            // use the chrom variable outside the try block
            System.out.println("Chromosome number: " + chrom);
               
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(panel, "Invalid chromosome number. Please enter an integer.");
            
        }
    }//GEN-LAST:event_ConfirmChrNoActionPerformed

